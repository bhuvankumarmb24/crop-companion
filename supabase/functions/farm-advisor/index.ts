import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          {
            role: "system",
            content: `You are KisanMitra (Farmer's Friend), an expert AI agricultural advisor. You help farmers with:

1. **Crop Selection**: Suggest suitable crops based on soil type, climate, season, water availability, and region.
2. **Fertilizer Recommendations**: Recommend organic and chemical fertilizers with dosage, timing, and application methods.
3. **Problem Diagnosis & Solutions**: Identify crop diseases, pest infestations, nutrient deficiencies from descriptions, and suggest remedies.
4. **Best Practices**: Share irrigation tips, crop rotation advice, and sustainable farming techniques.

Guidelines:
- Keep answers practical, clear, and actionable for farmers with varying education levels.
- Use simple language. Avoid jargon unless explaining it.
- When suggesting chemicals, always mention safety precautions.
- Recommend organic/natural alternatives when possible.
- If the farmer's question is unclear, ask clarifying questions about their soil, region, season, or crop.
- Format responses with clear headings and bullet points for readability.
- Include estimated costs or quantities when relevant.`
          },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Too many requests. Please try again in a moment." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "AI credits exhausted. Please add funds." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(JSON.stringify({ error: "AI service error" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("farm-advisor error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
